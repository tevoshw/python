class cachorro():
    def __init__(self):
        pass
    def latir(self):
        print("O cachorro está latindo")

    def correr(self):
        print("O cachorro está correndo")

    def beber_agua(self):
        print("O cachorro está bebendo agua")

acoes = cachorro()

acoes.latir()
acoes.correr()
acoes.beber_agua()
