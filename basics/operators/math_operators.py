# ADDICTIONS OPERATORS ARE USE "+"
# SUBTRACTION OPERATORS ARE USE "-"
# MULTIPLICATION OPERATORS ARE USE "*"
# DIVISION PPERATORS ARE USE "/"
# MODULES ARE USE (USE TO SEE THE REST) "%"
# POTENTINO ARE USE "**"
number1 = 10
number2 = 5

addiction = number1 + number2
print(f"The number are: {addiction}")

subtraction = number1 - number2
print(f"The number are: {subtraction}")

multiplication = number1 * number2
print(f"The number are: {multiplication}")

division = number1 / number2
print(f"The number are: {division}")

modules = number1 % number2
print(f"The number are: {modules}")


potention = number1 ** number2
print(f"The number are: {potention}")


