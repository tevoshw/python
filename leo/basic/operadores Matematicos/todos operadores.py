# + sinal de soma
# - sinal de subtração
# * sinal de multiplicação
# / sinal de divisão
# ** sinal de potencia
# // sianl divisão inteira
# % sinal de resto da divisão
var1=int(input('digite um numero: '))
var2=int(input('digite outro numero: '))
print(f'a soma de {var1} mais {var2} é igaul a {var1+var2}')
print(f'a subtração de  {var1} menos {var2} é igual a {var1-var2}')
print(f'a multiplicação de {var1} vesez {var2} é igual a {var1*var2}')
print(f'a divisão de {var1} divido por {var2} é igaul a {var1/var2}')
print(f'a portencia de {var1**2} é igual a{var1**2}')
print(f'a divisao inteira de {var1} por {var2} é igual a {var1//var2}')
print(f'o resto da divisão de {var1} por {var2} é igual a {var1%var2}')