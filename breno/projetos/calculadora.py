n1 = int(input("Digite o primeiro número: "))
n2 = int(input("Digite o segundo número: "))

print(f"O resultado da soma dos dois digitos é {n1 + n2}.")
print(f"O resultado da subtração dos dois digitos é {n1 - n2}.")
print(f"O resultado da multiplicação dos dois digitos é {n1 * n2}.")
print(f"O resultado da divisão dos dois digitos é {n1 / n2}.")