#inteiro para float
n_inteiro = 10
conversor = float(n_inteiro)
print(f'numero antes da conversão {n_inteiro}, numero depois da conversão {conversor}')

#float para inteiro
n_float = 3.14
conversor1 = int(n_float)
print(f'numero antes da conversao {n_float}, numero depois da conversão {conversor1}')

#inteiro para string
conversor2 = str(n_inteiro)
print(f"antes da conversão {n_inteiro}, depois da conversão {conversor2}")